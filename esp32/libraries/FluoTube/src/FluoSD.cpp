#include "FluoSD.h"
#include <SPI.h>

uint64_t cardSize_I;
uint8_t cardType_I; 

SPIClass SPISD_I(VSPI);

void SDCardInit()
{

    SPISD_I.begin(18,19,23);
 
    if(! SD.begin(SD_CS, SPISD_I, 4000000, "/sd") ){
      Serial.println("[SDCardInit] Card Mount Failed");
      return;
    }
  
    cardType_I = SD.cardType();

    if(cardType_I == CARD_NONE){
        Serial.println("[SDCardInit] No SD card valid");
        return;
    }

    Serial.println("[SDCardInit] SD Card Mount");
    Serial.println("[SDCardInit] SD Card Type: ");
    
    if(cardType_I == CARD_MMC){
        Serial.println("MMC");
    } else if(cardType_I == CARD_SD){
        Serial.println("SDSC");
    } else if(cardType_I == CARD_SDHC){
        Serial.println("SDHC");
    } else {
        Serial.println("UNKNOWN");
    }

    cardSize_I = SD.cardSize() / (1024 * 1024);

    Serial.printf("[SDCardInit] SD Card Mount, Size: %lluMB\n", cardSize_I); // debug always enable

    listDir_I(SD, "/", 0);
}

